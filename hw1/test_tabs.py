# This is a Python program that demonstrates the use of an actual tab character inside a string.
# Function to print a message with an actual tab character in the string
def print_with_actual_tab():
	# This string contains an actual tab character
	message = "Hello	World" # The space between "Hello" and "World" is an actual tab character
	print(message)
# Main program
if __name__ == "__main__":
    # Calling the function that prints a message with an actual tab character
    print_with_actual_tab()
